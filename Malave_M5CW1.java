public class Malave_M5CW1 {
    public static void main(String[] args) {
        int sum = 0;

        // Using a for loop to add numbers from 1 to 5
        for (int i = 1; i <= 5; i++) {
            sum += i;
        }

        System.out.println("The sum of numbers from 1 to 5 is: " + sum);
    }
}